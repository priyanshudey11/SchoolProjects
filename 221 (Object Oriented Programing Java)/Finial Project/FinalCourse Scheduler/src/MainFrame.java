    private void SceduleStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SceduleStudentActionPerformed
        ArrayList<CourseEntry> courses = Queries.getcourse(currentSemester);
        ArrayList<String> courseDescriptions = new ArrayList<>();
        String student = SelectStudent.getSelectedItem().toString();
        String courseDescription = SelectCourse.getSelectedItem().toString();
        String stat= "Enrolled";

        //gets seats
        int seats=0;
           for (CourseEntry course : courses) {
                String description = course.getCourseDescruption();
                courseDescriptions.add(description);
                if (description.equals(courseDescription)) {
                    seats = course.getSeats();
                    break;
                }
           }
            //updates availble seats
            if (seats <1) {
                stat = "Waitlisted";
                seats = seats - 1;
                Queries.updateSeats(courseDescription, seats);
                CourseAddSatbutton.setText("Student: " + student + " has been added Waitlisted to: "+ courseDescription+" to the: "+currentSemester+" semester");
            } 
            else {
                seats = seats - 1;
                Queries.updateSeats(courseDescription, seats);
                stat = "Enrolled";
                CourseAddSatbutton.setText("Student: " + student + " has been Enrolled to: "+ courseDescription+" to the: "+currentSemester+" semester");
            }
            seats = 0;

        String time = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime()).toString();
        Queries.addSchedule(new ScheduleEntry(currentSemester, student, courseDescription, stat, time));            
        

    }//GEN-LAST:event_SceduleStudentActionPerformed

