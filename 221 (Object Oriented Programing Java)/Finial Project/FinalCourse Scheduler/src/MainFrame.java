     public void rebuildSemesterComboBoxes() {
        ArrayList<String> semesters = Queries.getSemesterList();
        currentSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel(semesters.toArray()));

        if (!semesters.isEmpty()) {
            if (currentSemester == null || currentSemester.isEmpty()) {
                currentSemester = semesters.get(0);
                
            }
            
            currentSemesterLabel.setText(currentSemester);
            currentSemesterComboBox.setSelectedItem(currentSemester);
        } else {
            currentSemesterLabel.setText("None, add a semester.");
            currentSemester = "None";
        }
     
    }
   public void rebuildStudentComboBoxes() {
        ArrayList<StudentEntry> students = Queries.getallStudents();
        ArrayList<String> studentNames = new ArrayList<>();
    
        for (StudentEntry student : students) {
            String name = student.getLastname() + ", " + student.getFirstname();
            studentNames.add(name);
        }
        
        // Update the combo box models with the new student names
        SelectedStudentDis.setModel(new javax.swing.DefaultComboBoxModel<>(studentNames.toArray(new String[0])));
        SelectStudent.setModel(new javax.swing.DefaultComboBoxModel<>(studentNames.toArray(new String[0])));
    }
   
 public void rebuildCourseComboBoxes() {
        ArrayList<CourseEntry> courses = Queries.getcourse(currentSemester);
        ArrayList<String> courseID = new ArrayList<>();
    
        for (CourseEntry course : courses) {
            String id = course.getCourseDescruption();
            courseID.add(id);
        }
        SelectCourse.setModel(new javax.swing.DefaultComboBoxModel<>(courseID.toArray(new String[0])));
        

    }

   


